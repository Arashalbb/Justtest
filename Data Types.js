//JavaScript Data Types

//Number
//String
//Boolean
//Null
//Undefined




// Example for String
let str = "Hello, World!";
console.log(typeof str); // Output: "string"

// Example for Boolean
let isTrue = true;
console.log(typeof isTrue); // Output: "boolean"

// Example for Number
let num = 42;
console.log(typeof num); // Output: "number"

// Example for Null
let emptyValue = null;
console.log(typeof emptyValue); // Output: "object"
// Note: This is a well-known quirk in JavaScript; `null` is of type "object"

// Example for Undefined
let notAssigned;
console.log(typeof notAssigned); // Output: "undefined"
